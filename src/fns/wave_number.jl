function wave_number(F,H,g)

    e1 = 4*pi^2*F^2*H/g
    e2 = 1+0.6666666*e1+0.355555555*e1^2+0.1608465608*e1^3+
         0.0632098765*e1^4+0.0217540484*e1^5+0.0065407983*e1^6
    e3 = +e1^2+e1/e2
    K1 = sqrt(e3)/H
    o1 = sqrt(g*K1.*tanh(K1.*H))
    e1 = o1^2*H/g 
    e2 = 1+0.6666666*e1+0.355555555*e1^2+0.1608465608*e1^3+
        0.0632098765*e1^4+0.0217540484*e1^5+0.0065407983*e1^6
    e3 = +e1^2+e1/e2
    K2 = sqrt(e3)/H
    k = 2*K1-K2

    return k

end